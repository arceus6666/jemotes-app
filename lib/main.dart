class FacesScreen extends StatelessWidget {
  final String title;
  final List<String> faces;

  FacesScreen({Key key, @required this.title, @required this.faces})
      : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text(
          title,
          style: TextStyle(
            color: Color.fromRGBO(156, 4, 4, 1),
          ),
        ),
        backgroundColor: Color.fromRGBO(67, 5, 5, 1),
      ),
      body: new Builder(
        builder: (BuildContext context) {
          return ListView.builder(
            itemCount: faces.length,
            itemBuilder: (context, index) {
              return Padding(
                padding: const EdgeInsets.all(10.0),
                child: Card(
                  child: ListTile(
                    title: Text(
                      faces[index],
                      style: TextStyle(
                        color: Colors.white,
                        fontSize: 20,
                      ),
                    ),
                    trailing: Icon(
                      Icons.content_copy,
                      color: Colors.white,
                    ),
                    onTap: () {
                      Clipboard.setData(ClipboardData(text: faces[index]));
                      final sb = SnackBar(
                        content: Text(
                          'Copied',
                          textAlign: TextAlign.center,
                        ),
                        duration: const Duration(milliseconds: 1500),
                        backgroundColor: Colors.black.withOpacity(0.5),
                      );
                      Scaffold.of(context).showSnackBar(sb);
                    },
                  ),
                  color: Color.fromRGBO(104, 8, 8, 1),
                ),
              );
            },
          );
        },
      ),
      backgroundColor: Color.fromRGBO(4, 4, 4, 1),
    );
  }
}
